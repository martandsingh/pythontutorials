_LONG_COMMENT = "-" * 50

print(_LONG_COMMENT) # This will print "-" 50 times.
print("Typecasting Operations")
print(_LONG_COMMENT) # This will print "-" 50 times.

X = int("3") # Converting string "3" to int 3
Y = str(4) # Converting int 4 to string
P = float(5) # Converting integer 5 to float 5.0
Q = float("5.4") # Converting string to float
R = int(4.6) # Converting Float to Int - 4
print("> String to Integer: ", X)
print("> Integer to String: ",  Y)
print("> Integer to Float: ", P)
print("> String to Float: ", Q)
print("> Float to Integer: ", R)

print(_LONG_COMMENT) # This will print "-" 50 times.